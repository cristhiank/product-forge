#!/usr/bin/env bash
set -euo pipefail

# Build the Forge-GPT plugin for distribution.
#
# Assembles a standalone Forge-GPT plugin bundle from source:
#   - agents/forge-gpt/forge-gpt.agent.md → dist-forge-gpt/agents/Forge-GPT.agent.md (with frontmatter)
#   - agents/forge-gpt/SKILL.md → dist-forge-gpt/skills/forge-gpt/SKILL.md
#   - agents/forge-gpt/modes/{execute,verify}.md → dist-forge-gpt/skills/forge-{mode}-gpt/SKILL.md
#   - agents/forge-gpt/schemas/*.md → dist-forge-gpt/skills/forge-*/references/schemas/
#   - agents/forge/modes/{explore,ideate,design,plan,memory}.md → dist-forge-gpt/skills/forge-{mode}/SKILL.md (shared)
#   - agents/forge/shared/* → dist-forge-gpt/skills/forge-gpt/references/shared/ (shared prefs)
#   - agents/forge/product-hub/ → dist-forge-gpt/skills/forge-product/ (bundle + refs)
#   - skills/* → dist-forge-gpt/skills/* (infrastructure + architecture + PM skills)
#   - plugin.json (rewritten as forge-gpt) → dist-forge-gpt/plugin.json
#
# Usage:
#   ./build-forge-gpt-plugin.sh              # build to dist-forge-gpt/
#   ./build-forge-gpt-plugin.sh --install    # build + copilot plugin install
#   ./build-forge-gpt-plugin.sh --dry-run    # preview without writing

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
DIST="$SCRIPT_DIR/dist-forge-gpt"
DRY_RUN=false
INSTALL=false

for arg in "$@"; do
  case "$arg" in
    --dry-run) DRY_RUN=true ;;
    --install) INSTALL=true ;;
    *) echo "Unknown: $arg"; exit 1 ;;
  esac
done

echo "🤖 Forge-GPT Plugin Builder"
echo "   Source: $SCRIPT_DIR"
echo "   Output: $DIST"
echo ""

SUCCESS=0
TOTAL=0

# --- Helpers ---
copy_file() {
  local src="$1" dst="$2" label="$3"
  TOTAL=$((TOTAL + 1))
  if [ ! -f "$src" ]; then
    echo "   ❌ Missing: $src"
    return 1
  fi
  if $DRY_RUN; then
    echo "   Would copy: $label"
  else
    mkdir -p "$(dirname "$dst")"
    cp "$src" "$dst"
    echo "   ✅ $label"
  fi
  SUCCESS=$((SUCCESS + 1))
}

copy_dir() {
  local src="$1" dst="$2" label="$3"
  TOTAL=$((TOTAL + 1))
  if [ ! -d "$src" ]; then
    echo "   ❌ Missing dir: $src"
    return 1
  fi
  if $DRY_RUN; then
    echo "   Would copy dir: $label"
  else
    mkdir -p "$dst"
    cp -r "$src"/* "$dst"/
    echo "   ✅ $label"
  fi
  SUCCESS=$((SUCCESS + 1))
}

# --- Clean ---
if ! $DRY_RUN; then
  rm -rf "$DIST"
  mkdir -p "$DIST"
fi

# --- Step 1: Plugin manifest (rewritten for forge-gpt) ---
echo "📦 Plugin manifest..."
TOTAL=$((TOTAL + 1))
if [ -f "$SCRIPT_DIR/plugin.json" ]; then
  if $DRY_RUN; then
    echo "   Would write: plugin.json (forge-gpt manifest)"
  else
    mkdir -p "$DIST"
    python3 -c "
import json, sys
with open('$SCRIPT_DIR/plugin.json') as f: m = json.load(f)
m['name'] = 'forge-gpt'
m['description'] = 'Forge-GPT dev partner — GPT-family optimized dispatch coordinator with contract-driven execution, verification, and shared Forge exploration modes.'
kw = list(set(['forge-gpt','gpt-family','contract-driven'] + m.get('keywords',[])))
m['keywords'] = kw
with open('$DIST/plugin.json', 'w') as f: json.dump(m, f, indent=2); f.write('\n')
"
    echo "   ✅ plugin.json (forge-gpt manifest)"
  fi
  SUCCESS=$((SUCCESS + 1))
else
  echo "   ❌ Missing: plugin.json"
fi

# --- Step 2: Agent ---
echo ""
echo "🤖 Agent..."

AGENT_SRC="$SCRIPT_DIR/agents/forge-gpt/forge-gpt.agent.md"
AGENT_DST="$DIST/agents/Forge-GPT.agent.md"
TOTAL=$((TOTAL + 1))

if [ -f "$AGENT_SRC" ]; then
  BODY=$(awk 'NR>1 && /^[#*>\[]/{found=1} found{print}' "$AGENT_SRC")
  if [ -z "$BODY" ]; then
    BODY=$(tail -n +3 "$AGENT_SRC")
  fi

  FRONTMATTER="---
description: 'Contract-driven GPT coordinator. Routes work, constructs Mission Briefs, validates REPORTs, and stops.'
model: gpt-5.4
tools: [\"*\"]
---"

  RENDERED="${FRONTMATTER}

<!-- AUTO-GENERATED by build-forge-gpt-plugin.sh from forge-gpt.agent.md. DO NOT EDIT. -->

${BODY}
"

  if $DRY_RUN; then
    echo "   Would write: agents/Forge-GPT.agent.md (${#RENDERED} chars)"
  else
    mkdir -p "$DIST/agents"
    printf '%s' "$RENDERED" > "$AGENT_DST"
    echo "   ✅ agents/Forge-GPT.agent.md (${#RENDERED} chars)"
  fi
  SUCCESS=$((SUCCESS + 1))
else
  echo "   ❌ Missing: $AGENT_SRC"
fi

# --- Step 3: GPT coordinator skill ---
echo ""
echo "📜 GPT coordinator skill..."
copy_file \
  "$SCRIPT_DIR/agents/forge-gpt/SKILL.md" \
  "$DIST/skills/forge-gpt/SKILL.md" \
  "skills/forge-gpt/SKILL.md"

# --- Step 4: GPT mode skills ---
echo ""
echo "⚙️  GPT mode skills..."
for mode in "execute" "verify"; do
  copy_file \
    "$SCRIPT_DIR/agents/forge-gpt/modes/${mode}.md" \
    "$DIST/skills/forge-${mode}-gpt/SKILL.md" \
    "skills/forge-${mode}-gpt/SKILL.md"
done

# --- Step 5: Shared forge mode skills ---
echo ""
echo "🔁 Shared forge modes..."
SHARED_MODES=("explore" "ideate" "design" "plan" "memory")
for mode in "${SHARED_MODES[@]}"; do
  copy_file \
    "$SCRIPT_DIR/agents/forge/modes/${mode}.md" \
    "$DIST/skills/forge-${mode}/SKILL.md" \
    "skills/forge-${mode}/SKILL.md"
done

# --- Step 6: Product skill (shared) ---
echo ""
echo "📦 Product skill..."
copy_file \
  "$SCRIPT_DIR/agents/forge/modes/product.md" \
  "$DIST/skills/forge-product/SKILL.md" \
  "skills/forge-product/SKILL.md"

if [ -f "$SCRIPT_DIR/agents/forge/product-hub/scripts/index.js" ]; then
  copy_file \
    "$SCRIPT_DIR/agents/forge/product-hub/scripts/index.js" \
    "$DIST/skills/forge-product/scripts/index.js" \
    "skills/forge-product/scripts/index.js"
else
  echo "   ⚠️  Product-hub not built. Run: cd agents/forge/product-hub && npm run build"
fi
if [ -d "$SCRIPT_DIR/agents/forge/product-hub/references" ]; then
  copy_dir \
    "$SCRIPT_DIR/agents/forge/product-hub/references" \
    "$DIST/skills/forge-product/references" \
    "skills/forge-product/references/"
fi

# --- Step 7: GPT schema references ---
echo ""
echo "🧾 Schema references..."
SCHEMA_TARGETS=("forge-gpt" "forge-execute-gpt" "forge-verify-gpt")
SCHEMA_FILES=("mission-brief.v1.md" "report.v1.md")
for target in "${SCHEMA_TARGETS[@]}"; do
  for schema in "${SCHEMA_FILES[@]}"; do
    copy_file \
      "$SCRIPT_DIR/agents/forge-gpt/schemas/${schema}" \
      "$DIST/skills/${target}/references/schemas/${schema}" \
      "skills/${target}/references/schemas/${schema}"
  done
done

# --- Step 8: Shared engineering preferences ---
echo ""
echo "📋 Shared preferences..."
if [ -d "$SCRIPT_DIR/agents/forge/shared" ]; then
  for f in "$SCRIPT_DIR/agents/forge/shared"/*.md; do
    fname=$(basename "$f")
    copy_file "$f" "$DIST/skills/forge-gpt/references/shared/$fname" "skills/forge-gpt/references/shared/$fname"
  done
fi

# --- Step 9: Forge references (for shared modes) ---
echo ""
echo "📚 Forge references..."
if [ -d "$SCRIPT_DIR/agents/forge/references" ]; then
  for f in "$SCRIPT_DIR/agents/forge/references"/*.md; do
    fname=$(basename "$f")
    copy_file "$f" "$DIST/skills/forge/references/$fname" "skills/forge/references/$fname"
  done
fi

# Forge schemas (Claude-optimized, for shared modes that reference them)
if [ -d "$SCRIPT_DIR/agents/forge/schemas" ]; then
  for f in "$SCRIPT_DIR/agents/forge/schemas"/*.md; do
    fname=$(basename "$f")
    copy_file "$f" "$DIST/skills/forge/references/schemas/$fname" "skills/forge/references/schemas/$fname"
  done
fi

# --- Step 10: Infrastructure skills ---
echo ""
echo "🔧 Infrastructure skills..."
INFRA_SKILLS=("experts-council" "backlog" "agents-hub" "copilot-cli-skill")
for skill in "${INFRA_SKILLS[@]}"; do
  src_dir="$SCRIPT_DIR/skills/$skill"
  dst_dir="$DIST/skills/$skill"
  if [ ! -d "$src_dir" ]; then
    echo "   ❌ Missing: skills/$skill/"
    TOTAL=$((TOTAL + 1))
    continue
  fi
  copy_file "$src_dir/SKILL.md" "$dst_dir/SKILL.md" "skills/$skill/SKILL.md"
  if [ -f "$src_dir/scripts/index.js" ]; then
    copy_file "$src_dir/scripts/index.js" "$dst_dir/scripts/index.js" "skills/$skill/scripts/index.js"
  fi
  if [ -d "$src_dir/references" ]; then
    copy_dir "$src_dir/references" "$dst_dir/references" "skills/$skill/references/"
  fi
done

# --- Step 11: Architecture skills ---
echo ""
echo "🏗️  Architecture skills..."
ARCH_SKILLS=("backend-architecture" "frontend-architecture")
for skill in "${ARCH_SKILLS[@]}"; do
  src_dir="$SCRIPT_DIR/skills/$skill"
  dst_dir="$DIST/skills/$skill"
  if [ ! -d "$src_dir" ]; then
    echo "   ❌ Missing: skills/$skill/"
    TOTAL=$((TOTAL + 1))
    continue
  fi
  copy_file "$src_dir/SKILL.md" "$dst_dir/SKILL.md" "skills/$skill/SKILL.md"
  if [ -d "$src_dir/references" ]; then
    copy_dir "$src_dir/references" "$dst_dir/references" "skills/$skill/references/"
  fi
done

# --- Step 12: Product management skills ---
echo ""
echo "📋 Product management skills..."
PM_SKILLS=("jobs-to-be-done" "made-to-stick" "copywriting" "lean-startup" "storybrand-messaging" "cro-methodology" "page-cro")
for skill in "${PM_SKILLS[@]}"; do
  src_dir="$SCRIPT_DIR/skills/$skill"
  dst_dir="$DIST/skills/$skill"
  if [ ! -d "$src_dir" ]; then
    echo "   ⚠️  Optional: skills/$skill/ not found (skipping)"
    continue
  fi
  copy_file "$src_dir/SKILL.md" "$dst_dir/SKILL.md" "skills/$skill/SKILL.md"
  if [ -d "$src_dir/references" ]; then
    copy_dir "$src_dir/references" "$dst_dir/references" "skills/$skill/references/"
  fi
done

# --- Cleanup artifacts ---
if ! $DRY_RUN; then
  find "$DIST" -name '.DS_Store' -delete 2>/dev/null || true
fi

# --- Summary ---
echo ""
echo "═══════════════════════════════════════════════"
if $DRY_RUN; then
  echo "📊 Dry-run: would build $SUCCESS/$TOTAL components"
else
  echo "✅ Built $SUCCESS/$TOTAL components → $DIST/"
  echo ""
  echo "Plugin structure:"
  find "$DIST" -type f | sed "s|$DIST/|  |" | sort | head -50
  TOTAL_SIZE=$(du -sh "$DIST" | cut -f1)
  echo ""
  echo "Total size: $TOTAL_SIZE"
fi

# --- Install ---
if $INSTALL && ! $DRY_RUN; then
  echo ""
  echo "🚀 Installing plugin..."
  copilot plugin uninstall forge-gpt 2>/dev/null || true
  copilot plugin install "$DIST"
  echo ""
  copilot plugin list
fi

echo ""
echo "═══════════════════════════════════════════════"
[ "$SUCCESS" -eq "$TOTAL" ] || exit 1
